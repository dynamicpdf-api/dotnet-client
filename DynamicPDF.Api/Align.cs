
namespace DynamicPDF.Api
{
    /// <summary>
    /// Specifies horizontal alignment. Center alignment is the default value for alignments.
    /// </summary>
    public enum Align
    {
        /// <summary>
        /// Align left.
        /// </summary>
        Left,
        /// <summary>
        /// Align center.
        /// </summary>
        Center,
        /// <summary>
        /// Align right.
        /// </summary>
        Right
    }
}
